package com.android.ebook.data;

public class Book {

	String BookName;
    String BookPath;
 
	public String getBookName() {
		return BookName;
	}
	public void setBookName(String bookName) {
		BookName = bookName;
	}
	public String getBookPath() {
		return BookPath;
	}
	public void setBookPath(String bookPath) {
		BookPath = bookPath;
	}
}
